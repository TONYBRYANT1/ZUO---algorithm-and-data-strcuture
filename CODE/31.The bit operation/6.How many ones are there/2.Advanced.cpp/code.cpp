// 多少个 1

// 方案：
// 0）有一个数 A：1111 0101
//
// 1）用长度 1 记录 1 的个数：1 1 1 1 0 1 0 1
//                              
// 2）用长度 2 记录 1 的个数：10 10 01 01
// 
// 3）用长度 4 记录 1 的个数：0100 0010
//
// 4）用长度 8 记录 1 的个数：00000110

// 解答：
//
// 1）怎么用长度 1 记录 1 的个数：1 1 1 1 0 1 0 1
//    就是数本身
//
// 2）怎么用长度 2 记录 1 的个数：10 10 01 01
//     1)  A: 1111 0101                2) A>>1             3)  A: 0111 1010
//            0101 0101 &                 A: 0111 1010            0101 0101 &
//            -----------                                         -----------
//            0101 0101   ==> x1                                  0101 0000   ==> x2
//     
//     4) x1: 0101 0101
//        x2: 0101 0000 +
//            -----------
//            1010 0101
//
// 3）怎么用长度 4 记录 1 的个数：0100 0010
//     1)  A: 1010 0101                2) A>>2             3)  A: 0010 1001
//            0011 0011 &                 A: 0010 1001            0011 0011 &
//            -----------                                         -----------
//            0010 0001   ==> x1                                  0010 0001   ==> x2
//     
//     4) x1: 0010 0001
//        x2: 0010 0001 +
//            -----------
//            0100 0010
//
// 4）怎么用长度 8 记录 1 的个数：00000110
//     1)  A: 0100 0010                2) A>>4             3)  A: 0000 0100
//            0000 1111 &                 A: 0000 0100            0000 1111 &
//            -----------                                         -----------
//            0000 0010   ==> x1                                  0000 0100   ==> x2
//     
//     4) x1: 0000 0010
//        x2: 0000 0100 +
//            -----------
//            0000 0110
#include<bits/stdc++.h>

using namespace std;

typedef unsigned int ut;

ut CountBit_1(ut x)
{
    //用长度 2 记录 1 的个数
    x = (x & 0x55555555) + ((x>>1) & 0x55555555); // 5:0101
    //用长度 4 记录 1 的个数
    x = (x & 0x33333333) + ((x>>2) & 0x33333333); // 3:0011
    //用长度 8 记录 1 的个数
    x = (x & 0x0f0f0f0f) + ((x>>4) & 0x0f0f0f0f); // 0:0000 f:1111
    //用长度 16 记录 1 的个数
    x = (x & 0x00ff00ff) + ((x>>8) & 0x00ff00ff); // 0:0000 f:1111
    //用长度 16 记录 1 的个数
    x = (x & 0x0000ffff) + ((x>>16) & 0x0000ffff); // 0:0000 f:1111

    return x;
}

int main()
{
    ut a;
    cin >> a;
    printf("%d" , CountBit_1(a));

    return 0;
}

