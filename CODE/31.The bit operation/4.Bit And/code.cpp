#include<bits/stdc++.h>

using namespace std;

unsigned int RightOne(unsigned int x)
{
    return x & (~x+1);
}

unsigned int BitAnd(unsigned int a , unsigned int b)
{
    // left:a  right:b
    while(b>a)
    {
        b -= RightOne(b);
    }
    return b;
}

int main()
{
    unsigned int a , b , c;
    cin >> a >> b;
    c = BitAnd(a , b);
    printf("%d" , c);
    return 0;
}


